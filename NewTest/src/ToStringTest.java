public class ToStringTest {
    private int id=100;
    private String name ="Buddhima";

    @Override
    public String toString() {
        return "ToStringTest{" +
                "id=" + id +
                ", name='" + name + '\'' +
                '}';
    }

    public void setId(int id) {
        this.id = id;
    }

    public void setName(String name) {
        this.name = name;
    }

    public int getId() {
        return id;
    }

    public String getName() {
        return name;
    }
}
